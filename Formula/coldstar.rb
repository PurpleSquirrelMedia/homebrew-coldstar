  def install
    # Build and install Rust secure_signer component
    cd "secure_signer" do
      system "cargo", "install", *std_cargo_args
    end

    # Create virtualenv and install pure Python dependencies
    venv = virtualenv_create(libexec, "python3.12")
    venv.pip_install resources.reject { |r| ["solana", "solders"].include?(r.name) }

    # Install solana and solders using pip (uses pre-built wheels)
    system libexec/"bin/pip", "install", "solana==0.36.10", "solders==0.27.1"

    # Install application files to libexec
    libexec.install "main.py"
    libexec.install "src"
    libexec.install "config.py"
    libexec.install "flash_usb.py"
    libexec.install "upgrade_wallet.py"
    libexec.install "python_signer_example.py"
    libexec.install "secure_signer"

    # Create executable wrapper
    (bin/"coldstar").write <<~EOS
      #!/bin/bash
      export COLDSTAR_HOME="#{libexec}"
      cd "#{libexec}"
      exec "#{libexec}/bin/python3" "#{libexec}/main.py" "$@"
    EOS
  end

  def caveats
    <<~EOS
      ColdStar has been installed!

      To run ColdStar:

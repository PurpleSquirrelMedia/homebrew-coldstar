      Note: ColdStar requires USB drive access for cold wallet operations.
      On macOS, you may need to grant terminal disk access permissions
      in System Preferences > Security & Privacy > Privacy > Full Disk Access.
    EOS
  end

  test do
    # Test that the wrapper script exists and is executable
    assert_path_exists bin/"coldstar"
